#include <stdio.h>

int	ft_strncmp(char *s1, char *s2, unsigned int n)
{
	unsigned int i;

	i = 0;
	while ((s1[i] || s2[i]) && i < n)
	{
		if (s1[i] != s2[i])
			return (s1[i] - s2[i]);
		i++;
	}
	return (0);
}


int main(void)
{
    char a[] = "baaaaaaa";
    char b[] = "baaaaaaaa";

    printf("%d", ft_strncmp(a, b, 4000));
    return(0);
}
