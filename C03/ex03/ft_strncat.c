#include <stdio.h>

char	*ft_strncat(char *dest, char *src, unsigned int nb)
{
	unsigned int i;
	unsigned int j;

	i = 0;
	j = 0;
	while (dest[i] != '\0')
	{
		i++;
	}
	while (src[j] != '\0' && j < nb)
	{
		dest[i + j] = src[j];
		j++;
	}
	dest[i + j] = '\0';
	return (dest);
}




int main(void)
{
    char src [20] = "y adios";
    char dest [20] = "hola";

    ft_strncat(dest, src, 2);
    printf("%s\n", dest);
    return (0);
}
