#include <stdio.h>
#include <string.h>

int	ft_strlen(char *str)
{
	int	index;

	index = 0;
	while (str[index])
		index++;
	return (index);
}

int	main(void)
{
	char str[] = "holaa";

	printf("%d", ft_strlen(str));

}